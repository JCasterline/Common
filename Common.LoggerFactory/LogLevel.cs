        /// Logs general information.
        /// </summary>
        Info,
        /// <summary>
        /// Logs warning information.
        /// </summary>
        Warn,
        /// <summary>
        /// Logs error information.
        /// </summary>
        Error,
        /// <summary>
        /// Logs fatal error information.
        /// </summary>
        Fatal
    }
}
